  onColourSchemeChange: () => void;
  onLineThicknessInput: () => void;
  onConnectionTypeChange: () => void;
  onLineEndpointChange: () => void;
  resetViewAndCloseMoreMenu: () => void;
  showMapView: () => void;
  showOutlineView: () => void;
  onTitleInput: () => void;
  showDetailsTab: () => void;
  showNotesTab: () => void;
  showLinksTab: () => void;
  onKindChange: () => void;
  onKindDefaultClick: () => void;
  addKindFromSettings: () => void;
