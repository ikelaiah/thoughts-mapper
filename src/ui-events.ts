  startLeftPanelResize: (event: PointerEvent) => void;
  startRightPanelResize: (event: PointerEvent) => void;
  resetLeftPanelWidth: () => void;
  resetRightPanelWidth: () => void;
  closeMobilePanels: () => void;
  switchProject: () => void;
  renameActiveProject: () => void;
  toggleNewProjectPanel: () => void;
  createProjectFromSelectedTemplate: () => void;
  renderThoughtList: () => void;
  onQuickCaptureSubmit: (event: SubmitEvent) => void;
  toggleMobileCapture: () => void;
  closeMobileCapture: () => void;
  onMobileCaptureSubmit: (event: SubmitEvent) => void;
  handleInboxFilterClick: () => void;
  exportMap: () => void;
  exportMarkdown: () => void;
  importMap: (event: Event) => void;
  toggleSidebar: () => void;
  toggleDetailsPanel: () => void;
  toggleMoreMenu: () => void;
  undoAndCloseMoreMenu: () => void;
  redoAndCloseMoreMenu: () => void;
